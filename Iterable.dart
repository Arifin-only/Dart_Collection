void main(List<String> args){
    //Iterable adalah jenis data yang bisa diulang satu per satu,
    //seperti daftar barang atau uruta angka. contoh dari iterable
    //adalah list dan set
    List<int> number = [1, 2, 3, 4, 5];//contoh dari iterable
    
    //menggunakan for-each untuk mengitegrasi setiap elemen
    for (int number in numbers){
        print(number);
    }
}
